public class NodeTime {
    private int node;
    private int time;


    public NodeTime(int node, int time) {
        this.node = node;
        this.time = time;
    }

    public int getTime() {
        return time;
    }

    public void setTime(int time) {
        this.time = time;
    }

    public int getNode() {
        return node;
    }

    public void setNode(int node) {
        this.node = node;
    }
}
